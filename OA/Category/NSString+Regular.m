+ (NSString *)parseTime:(NSString *)timeString{
    NSArray *array = [timeString componentsSeparatedByString:@"T"];
    NSString *str2 = [array[1] substringToIndex:8];
    NSString *str = [NSString stringWithFormat:@"%@ %@",array[0],str2];
    return str;
}



